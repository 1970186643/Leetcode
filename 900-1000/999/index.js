var numRookCaptures = function(board) {
  const len1 = board.length,
        len2 = board[0].length;
  let n1,n2,count = 0;
  for(let i = 0 ;i < len1 ;i++){
    for(let j = 0 ;j < len2 ;j++){
      if(board[i][j] == 'r' || board[i][j] == 'R'){
        n1 = i;
        n2 = j;
      }
    }
  }
  for(let i = n1 ;i >=0 ;i--){
    if(board[i][n2] == 'B')
      break;
    if(board[i][n2] == 'p'){
      count++;
      break;
    }  
  } 
  for(let i = n1 ;i <len2;i++){
    if(board[i][n2] == 'B')
      break;
    if(board[i][n2] == 'p'){
      count++;
      break;
    }  
  }
  for(let j = n2 ;j >=0; j--){
    if(board[n1][j] == 'B')
      break;
    if(board[n1][j] == 'p'){
      count++;
      break;
    }  
  }
  for(let j = n2 ;j <len1; j++){
    if(board[n1][j] == 'B')
      break;
    if(board[n1][j] == 'p'){
      count++;
      break;
    }  
  }  
  return count;
};

